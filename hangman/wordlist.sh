choose_word() {
  init_wordfile
  local w=""
  # so lange wiederholen, bis wir wirklich ein nicht‑leeres Wort haben
  while [[ -z "$w" ]]; do
    w=$(shuf -n1 "$wordfile" | tr -d '\r\n')
  done
  # einmalig anhängen, falls neu (nice‑to‑have)
  grep -qxF "$w" "$wordfile" || echo "$w" >> "$wordfile"
  echo "$w"
}
